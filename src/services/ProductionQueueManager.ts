  async addWebhookJob(
    eventId: string,
    payload: any,
    merchantId: string,
    platform: 'INSTAGRAM' | 'WHATSAPP' | 'FACEBOOK',
    priority: 'LOW' | 'MEDIUM' | 'HIGH' | 'CRITICAL' = 'MEDIUM'
  ): Promise<JobResult> {
    if (!this.queue) {
      return { 
        success: false, 
        error: 'ŸÖÿØŸäÿ± ÿßŸÑÿ∑Ÿàÿßÿ®Ÿäÿ± ÿ∫Ÿäÿ± ŸÖŸáŸäÿ£' 
      };
    }

    try {
      const jobData: QueueJob = {
        eventId,
        payload,
        merchantId,
        platform,
        priority,
        metadata: {
          addedAt: new Date().toISOString(),
          source: 'webhook'
        }
      };

      const priorityValue = this.getPriorityValue(priority);
      
      const job = await this.queue.add('process-webhook', jobData, {
        priority: priorityValue,
        delay: priority === 'CRITICAL' ? 0 : 100,
        removeOnComplete: priority === 'CRITICAL' ? 200 : 100,
        removeOnFail: priority === 'CRITICAL' ? 100 : 50,
        attempts: priority === 'CRITICAL' ? 5 : 3
      });

      // ÿßŸÑÿ≠ÿµŸàŸÑ ÿπŸÑŸâ ŸÖŸàŸÇÿπ ÿßŸÑŸÖŸáŸÖÿ© ŸÅŸä ÿßŸÑÿ∑ÿßÿ®Ÿàÿ±
      const waiting = await this.queue.getWaiting();
      const queuePosition = waiting.findIndex(j => j.id?.toString() === job.id?.toString()) + 1;

      this.logger.info('ÿ™ŸÖ ÿ•ÿ∂ÿßŸÅÿ© ŸÖŸáŸÖÿ© ŸàŸäÿ® ŸáŸàŸÉ ŸÑŸÑÿ∑ÿßÿ®Ÿàÿ±', {
        jobId: job.id,
        eventId,
        merchantId,
        platform,
        priority,
        queuePosition
      });

      return { 
        success: true, 
        jobId: job.id?.toString(),
        queuePosition
      };

    } catch (error) {
      this.logger.error('ŸÅÿ¥ŸÑ ŸÅŸä ÿ•ÿ∂ÿßŸÅÿ© ŸÖŸáŸÖÿ© ŸÑŸÑÿ∑ÿßÿ®Ÿàÿ±', { 
        eventId,
        merchantId,
        platform,
        error: error instanceof Error ? error.message : String(error)
      });
      
      return { 
        success: false, 
        error: error instanceof Error ? error.message : String(error)
      };
    }
  }

